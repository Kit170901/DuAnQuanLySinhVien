package entity;

/**
 *
 * @author quang
 */
public class Students {
    public String MaSV;
    public String HoTen;

    public Students() {
    }

    public Students(String MaSV, String HoTen) {
        this.MaSV = MaSV;
        this.HoTen = HoTen;
    }

    public String getMaSV() {
        return MaSV;
    }

    public void setMaSV(String MaSV) {
        this.MaSV = MaSV;
    }

    public String getHoTen() {
        return HoTen;
    }

    public void setHoTen(String HoTen) {
        this.HoTen = HoTen;
    }
    
}
